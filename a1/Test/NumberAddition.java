public class NumberAddition{
   public static void main(String[] args){
      int num1, num2, sum_of_numbers;
      num1 = 555;
      num2 = 55555;
      System.out.printf("The two numbers are %d and %d",num1, num2 );
      System.out.printf("\n");
      sum_of_numbers = num1 + num2;
      System.out.println("The numbers have been added using '+' operator");
      System.out.println("\nSum of the two numbers is : ");
      System.out.println(sum_of_numbers);
      System.out.printf("\n");
   }
}